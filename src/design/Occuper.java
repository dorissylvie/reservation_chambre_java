    public void afficherOccuper() {
        try {
            PreparedStatement req;
            ResultSet res;
            req = Connexion.connBD().prepareStatement("SELECT * FROM occuper order by idoccup ASC");
            res = req.executeQuery();
            ResultSetMetaData rs = res.getMetaData();

            DefaultTableModel df = (DefaultTableModel) tableauOccuper.getModel();
            df.setRowCount(0);
            while (res.next()) {
                Vector v = new Vector();

                v.add(res.getString("idoccup"));
                v.add(res.getString("idreserv"));

                df.addRow(v);
            }
        } catch (SQLException ex) {

        }
    }

    public void ajouterOccuper(int idreserv, int montant) {

        int conf = JOptionPane.showConfirmDialog(null, " Voulez-vous vraiment ajouter cette nouvelle occupation ?");

        if (conf == JOptionPane.YES_OPTION){
            PreparedStatement stmt;
            try {
                stmt = Connexion.connBD().prepareStatement("INSERT INTO occuper (idreserv) VALUES (?) ");
                stmt.setInt(1, idreserv);
                stmt.executeUpdate();
                updateSolde(montant);         
                afficherOccuper();

            } catch (Exception e) {
                System.out.println(e);
            }

        }

    }

    public void modifierOccuper(int idoccup, int idreserv, int montant) {

        int conf = JOptionPane.showConfirmDialog(null, " Voulez-vous vraiment modifier cette occupation ?");

        if (conf == JOptionPane.YES_OPTION) {
            PreparedStatement stmt;
            try {

                stmt = Connexion.connBD().prepareStatement("UPDATE occuper set idreserv = ?  where idoccup = ? ");
                stmt.setInt(1, idreserv);
                stmt.setInt(2, idoccup);
                stmt.executeUpdate();

                updateSolde(montant);

                afficherOccuper();

            } catch (Exception e) {
                System.out.println(e);
            }
        }

    }

    public void updateSolde(int montant) {
        PreparedStatement stmt;
        try {
            stmt = Connexion.connBD().prepareStatement("INSERT INTO solde (solde) VALUES (?)");
            stmt.setInt(1, montant);
            stmt.executeUpdate();
            solde.afficherSolde();
        } catch (Exception e) {
            System.out.println(e);
        }
   }

    public void supprimerOccuper(int idoccup, int montant) {

        int conf = JOptionPane.showConfirmDialog(null, " Voulez-vous vraiment supprimer cette occupation ?");

        if (conf == JOptionPane.YES_OPTION) {
            PreparedStatement stmt;
            try {

                stmt = Connexion.connBD().prepareStatement("DELETE FROM  occuper where idoccup = ? ");
                stmt.setInt(1, idoccup);
                stmt.executeUpdate();
                updateSolde(montant);
                afficherOccuper();
                
            } catch (Exception e) {
                System.out.println(e);
            }
        }

    }
  

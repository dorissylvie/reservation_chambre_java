    public void afficherMontant(int idreserv){
        try{
            PreparedStatement stmt = Connexion.connBD().prepareStatement("SELECT * FROM reserver where idreserv = ?");
            stmt.setInt(1,idreserv);
            ResultSet res = stmt.executeQuery();
            while (res.next()){

                String numchambre = res.getString("numchambre");
                int nbrjour = res.getInt("nbrjours");

                int solde = 0;
                String[] nums = numchambre.split(",");
                for (String num : nums) {
                    stmt = Connexion.connBD().prepareStatement(" SELECT  * from chambre where numchambre = ? ");
                    stmt.setString(1, num);

                    res = stmt.executeQuery();
                    while (res.next()) {
                        solde += (nbrjour * res.getInt("prixnuite"));
                    }
                }               
                montantOc.setText("" + solde + "");
                montantPrec.setValue((-1)*solde);
            
            }
        }
        catch( Exception e ){
            System.out.println(e);
        }  
    } 
  
